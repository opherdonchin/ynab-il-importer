def build_demo(output_dir: Path) -> tuple[Path, Path]:
    output_dir.mkdir(parents=True, exist_ok=True)
    proposed_path = output_dir / "review_app_demo_proposed.parquet"
    categories_path = output_dir / "review_app_demo_categories.csv"
    table = pa.Table.from_pylist(demo_review_records(), schema=REVIEW_SCHEMA)
    review_io.save_review_artifact(table, proposed_path)
    categories = pl.DataFrame(
        {
            "category_group": [
                "Living",
                "Living",
                "Living",
                "Living",
                "Internal Master Category",
            ],
            "category_name": [
                "Going out",
                "Books",
                "Gifts",
                "House and stuff",
                "Uncategorized",
            ],
        }
    )
    categories.write_csv(categories_path, include_bom=True)
    return proposed_path, categories_path


def _build_arg_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Build a canonical demo review artifact.")
    parser.add_argument(
        "--out-dir",
        default="data/demo/review_app",
        help="Directory where the demo parquet and categories CSV should be written.",
    )
    return parser


def main() -> None:
    args = _build_arg_parser().parse_args()
    proposed_path, categories_path = build_demo(Path(args.out_dir))
    print(f"Wrote demo review artifact to {proposed_path}")
    print(f"Wrote demo categories to {categories_path}")


if __name__ == "__main__":
    main()
