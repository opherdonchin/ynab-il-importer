    return {
        "split_id": split_id,
        "parent_transaction_id": parent_transaction_id,
        "ynab_subtransaction_id": split_id,
        "payee_raw": payee_raw,
        "category_id": "",
        "category_raw": category_raw,
        "memo": memo,
        "inflow_ils": inflow_ils,
        "outflow_ils": outflow_ils,
        "import_id": "",
        "matched_transaction_id": "",
    }


